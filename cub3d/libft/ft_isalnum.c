#include "libft.h"

int	ft_isalnum(int i)
{
	if (i > 64 && i < 91)
		return (1);
	else if (i > 96 && i < 123)
		return (1);
	else if (i > 47 && i < 58)
		return (1);
	else
		return (0);
}
